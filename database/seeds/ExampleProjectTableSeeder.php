HHI是什麼？#

Hip Hop International 世界街舞錦標賽簡稱HHI，從2002年由美國洛杉磯總部開始舉辦，電視節目在全球高達百餘個國家收看，街舞圈有「街舞奧運」之稱。

賽事評審需經過美國官方的指導員訓練才能擔任，分別針對規則、舞蹈呈現、畫面呈現等細項進行評分，堪比奧運賽事的嚴謹計分，樹立了HHI 在街舞圈的權威，每年更有多達50幾個國家的區賽冠軍將前往美國角逐世界殊榮，因此，許多舞者也將之視為「一生一定要參加一次的街舞大賽」。

