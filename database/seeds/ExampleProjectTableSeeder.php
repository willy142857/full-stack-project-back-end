惹內(Jean Genet)華麗的文字和詩意，讓「罪孽」一個個神聖了起來#
         《繁花聖母》為1930年代法國劇作家惹內之作品。惹內的一生始終不見容於現代社會秩序，由棄兒的身分到小偷、流浪者、賣淫者、同性戀者，他在教養院、監獄、路邊及勞改所度過的時光，以及這個曾經棄絕過他的社會，卻同時成為他的創作靈感。

           惹內將所處之地、所遇之人，寫進一部又一部的小說及劇本中，為一切被定義為罪惡的事，賦予莊嚴、神聖的美感，反映主流社會價值之外的底層生活。批判性的作品使他再再遭政府流放，卻因獲得法國當時存在主義大師沙特、藝術家科克多等人的支持，終獲總統特赦。 



